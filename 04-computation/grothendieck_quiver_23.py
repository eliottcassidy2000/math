# Gaussian binomial coefficients
def gauss_binom(n, k, q):
    """[n choose k]_q = product_{i=1}^{k} (q^{n-i+1} - 1)/(q^i - 1)"""
    if k < 0 or k > n:
        return 0
    num = 1
    den = 1
    for i in range(1, k+1):
        num *= (q**(n-i+1) - 1)
        den *= (q**i - 1)
    return num // den

