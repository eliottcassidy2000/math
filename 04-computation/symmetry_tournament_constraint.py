print(f"\n--- Part 4: n=3 check ---")
n3 = 3
t3 = {}
for i in range(n3):
    for j in range(n3):
        if i != j:
            t3[(i,j)] = symbols(f's{i}{j}')

def hp3(vset, end=None, start=None):
    vl = sorted(vset)
    if len(vl) <= 1:
        if start is not None and (len(vl)==0 or vl[0] != start): return 0
        if end is not None and (len(vl)==0 or vl[0] != end): return 0
        return 1 if len(vl)==1 else 0
    total = 0
    for p in permutations(vl):
        if start and p[0] != start: continue
        if end and p[-1] != end: continue
        prod = 1
        for i in range(len(p)-1):
            prod *= t3[(p[i], p[i+1])]
        total += prod
    return expand(total)

a3, b3 = 0, 1
U3 = [2]
M3_ab = 0
M3_ba = 0
for mask in range(1 << len(U3)):
    S = [U3[i] for i in range(len(U3)) if mask & (1 << i)]
    R = [U3[i] for i in range(len(U3)) if not (mask & (1 << i))]
    sign = (-1)**len(S)
    ea = hp3(set(S)|{a3}, end=a3)
    bb = hp3(set(R)|{b3}, start=b3)
    eb = hp3(set(S)|{b3}, end=b3)
    ba = hp3(set(R)|{a3}, start=a3)
    M3_ab += sign * ea * bb
    M3_ba += sign * eb * ba

M3_ab = expand(M3_ab)
M3_ba = expand(M3_ba)
diff3 = expand(M3_ab - M3_ba)
print(f"  M[0,1] = {M3_ab}")
print(f"  M[1,0] = {M3_ba}")
print(f"  Diff (general) = {diff3}")
print(f"  Is zero? {diff3 == 0}")

if diff3 != 0:
    subs3 = {t3[(j,i)]: 1 - t3[(i,j)] for i in range(n3) for j in range(i+1, n3)}
    diff3_tour = expand(diff3.subs(subs3))
    print(f"  Diff (tournament) = {diff3_tour}")

# ==========================================================
# Part 5: What if we relax to t_ij + t_ji = c (constant)?
# ==========================================================
print(f"\n--- Part 5: Relaxation t_ij + t_ji = c ---")
c = symbols('c')
if diff_general != 0:
    subs_c = {}
    for i in range(n):
        for j in range(i+1, n):
            subs_c[t[(j,i)]] = c - t[(i,j)]

    diff_c = expand(diff_general.subs(subs_c))
    print(f"  With t_ij + t_ji = c:")

    # Collect by powers of c
    from sympy import Poly as SPoly, degree as sdeg
    p = SPoly(diff_c, c)
    print(f"  Polynomial in c: degree {p.degree()}")
    for power in range(p.degree() + 1):
        coeff = expand(p.nth(power))
        if coeff != 0:
            print(f"    c^{power}: {coeff}")

    # At c=1 (tournament): should be 0
    at_1 = expand(diff_c.subs(c, 1))
    print(f"\n  At c=1 (tournament): {at_1}")
    # At c=0 (mutual exclusion possible):
    at_0 = expand(diff_c.subs(c, 0))
    print(f"  At c=0: {at_0}")

