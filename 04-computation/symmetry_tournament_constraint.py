print(f"\n--- Part 6: Numerical test with random digraphs ---")
import random
random.seed(42)

for n_test in [4, 5]:
    failures = 0
    for trial in range(100):
        # Random digraph (each arc independently with prob 0.5)
        T = [[0]*n_test for _ in range(n_test)]
        for i in range(n_test):
            for j in range(n_test):
                if i != j:
                    T[i][j] = random.randint(0, 1)

        # Check M[0,1] vs M[1,0]
        a_t, b_t = 0, 1
        U_t = [v for v in range(n_test) if v != a_t and v != b_t]
        m_ab = 0
        m_ba = 0
        for mask in range(1 << len(U_t)):
            S = [U_t[i] for i in range(len(U_t)) if mask & (1 << i)]
            R = [U_t[i] for i in range(len(U_t)) if not (mask & (1 << i))]
            sign = (-1)**len(S)

            def hp_n(vset, start=None, end=None):
                vl = sorted(vset)
                c = 0
                for p in permutations(vl):
                    if start is not None and p[0] != start: continue
                    if end is not None and p[-1] != end: continue
                    ok = True
                    for ii in range(len(p)-1):
                        if T[p[ii]][p[ii+1]] != 1:
                            ok = False
                            break
                    if ok: c += 1
                return c

            ea = hp_n(set(S)|{a_t}, end=a_t)
            bb = hp_n(set(R)|{b_t}, start=b_t)
            eb = hp_n(set(S)|{b_t}, end=b_t)
            ba = hp_n(set(R)|{a_t}, start=a_t)
            m_ab += sign * ea * bb
            m_ba += sign * eb * ba

        if m_ab != m_ba:
            failures += 1

    print(f"  n={n_test}: {failures}/100 random digraphs have M[0,1] != M[1,0]")

