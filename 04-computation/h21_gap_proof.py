from itertools import combinations
from collections import Counter
import time


def held_karp(n, adj):
    dp = [[0] * n for _ in range(1 << n)]
    for v in range(n):
        dp[1 << v][v] = 1
    for S in range(1, 1 << n):
        for v in range(n):
            if not (S & (1 << v)):
                continue
            c = dp[S][v]
            if c == 0:
                continue
            for u in range(n):
                if S & (1 << u):
                    continue
                if adj[v] & (1 << u):
                    dp[S | (1 << u)][u] += c
    return sum(dp[(1 << n) - 1])


def exhaustive_H_values(n):
    """Return set of all achievable H values at vertex count n."""
    edges = [(i, j) for i in range(n) for j in range(i + 1, n)]
    m = len(edges)
    H_counter = Counter()
    for bits in range(1 << m):
        adj = [0] * n
        for k, (i, j) in enumerate(edges):
            if bits & (1 << k):
                adj[j] |= 1 << i
            else:
                adj[i] |= 1 << j
        H_counter[held_karp(n, adj)] += 1
    return H_counter


def flip_analysis(n):
    """Analyze H values from transitive tournament + edge flips."""
    edges = [(i, j) for i in range(n) for j in range(i + 1, n)]
    m = len(edges)
    trans_adj = [0] * n
    for i in range(n):
        for j in range(i + 1, n):
            trans_adj[i] |= 1 << j

    # Single flips
    print(f"Single-flip H values (n={n}):")
    for idx in range(m):
        i, j = edges[idx]
        if i != 0:
            continue
        k = j - i
        adj = list(trans_adj)
        adj[i] &= ~(1 << j)
        adj[j] |= 1 << i
        H = held_karp(n, adj)
        is_power = (H - 1) > 0 and ((H - 1) & (H - 2)) == 0
        exp = (H - 1).bit_length() - 1 if is_power else "?"
        print(f"  range k={k}: H={H} = 1+2^{exp}")

    # Two non-overlapping flips
    print(f"\nNon-overlapping 2-flip multiplicativity check (n={n}):")
    for f1, f2 in combinations(range(m), 2):
        i1, j1 = edges[f1]
        i2, j2 = edges[f2]
        if max(i1, i2) <= min(j1, j2):  # overlapping
            continue
        k1, k2 = j1 - i1, j2 - i2
        if k1 < 2 or k2 < 2 or k1 > 4 or k2 > 4:
            continue
        if i1 != 0:
            continue
        adj = list(trans_adj)
        for f in [f1, f2]:
            i, j = edges[f]
            adj[i] &= ~(1 << j)
            adj[j] |= 1 << i
        H = held_karp(n, adj)
        predicted = (1 + 2 ** (k1 - 1)) * (1 + 2 ** (k2 - 1))
        match = H == predicted
        print(f"  ({k1},{k2}): H={H}, predicted={predicted}, match={match}")


