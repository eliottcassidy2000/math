def legendre(a, p):
    """Legendre symbol (a/p)."""
    if a % p == 0:
        return 0
    return pow(a, (p-1)//2, p) * 2 - (1 if pow(a, (p-1)//2, p) == 1 else -1) if pow(a, (p-1)//2, p) != 1 else 1

def get_QR(p):
    """Quadratic residues mod p."""
    return set(pow(a, 2, p) for a in range(1, p)) - {0}

def make_tournament(p, S):
    """Adjacency matrix for circulant tournament on Z_p with connection set S."""
    A = np.zeros((p, p), dtype=int)
    for i in range(p):
        for s in S:
            j = (i + s) % p
            A[i][j] = 1
    return A

def count_H(A):
    """Count Hamiltonian paths via DP bitmask."""
    n = len(A)
    if n > 20:
        return -1  # too large
    dp = np.zeros((1 << n, n), dtype=np.int64)
    for i in range(n):
        dp[1 << i][i] = 1
    full = (1 << n) - 1
    for mask in range(1, full + 1):
        for u in range(n):
            if not (mask & (1 << u)):
                continue
            if dp[mask][u] == 0:
                continue
            for v in range(n):
                if mask & (1 << v):
                    continue
                if A[u][v]:
                    dp[mask | (1 << v)][v] += dp[mask][u]
    return sum(dp[full])

def dihedral_orbit(S, p):
    """Compute the D_p orbit of connection set S.
    D_p is generated by rotation r: x -> x+1 and reflection s: x -> -x.
    For connection sets: rotation doesn't change S (circulant),
    reflection sends S -> {p-s : s in S} = complement connection set.
    Also multiply by units: a*S mod p for a in (Z/pZ)*.
    """
    S_set = frozenset(S)
    S_comp = frozenset((p - s) % p for s in S)
    return S_set, S_comp

def affine_orbit(S, p):
    """Full affine orbit: {a*S + b mod p : a in (Z/pZ)*, b in Z_p}.
    For connection sets of circulant tournaments, b doesn't matter.
    So the orbit is {a*S mod p : a in (Z/pZ)*}.
    """
    orbits = set()
    for a in range(1, p):
        aS = frozenset((a * s) % p for s in S)
        orbits.add(aS)
    return orbits

