# Each Vitali reversal is an involution: applying it twice returns the original
# But does B = reverse(A, S) also have S as a Vitali atom?
# i.e., is the reverse also lambda-preserving?

self_inverse_count = 0
reverse_also_vitali = 0

for ex in multi_atom_examples[:20]:
    A = ex['A']
    L = ex['L']
    for S in ex['vitali_subsets'][:3]:
        B = reverse_subtournament(A, n, list(S))

        # Check: is SS also (1,1,2,2) in B?
        ss_B = sub_scores(B, n, list(S))

        # Check: is reversal of S in B lambda-preserving?
        LB = lambda_graph(B, n)
        C = reverse_subtournament(B, n, list(S))  # should equal A

        self_inverse = np.array_equal(C, A)
        self_inverse_count += int(self_inverse)

        # Also check if S is Vitali in B
        ss_is_1122 = (ss_B == (1, 1, 2, 2))
        reverse_preserves = np.array_equal(LB, lambda_graph(C, n))
        if ss_is_1122 and reverse_preserves:
            reverse_also_vitali += 1

        if not self_inverse:
            print(f"  SELF-INVERSE FAILS! S={S}")

print(f"\nSelf-inverse: {self_inverse_count} / {self_inverse_count} (always true by construction)")
print(f"Reverse also Vitali: {reverse_also_vitali} (does B also have S as Vitali atom?)")

