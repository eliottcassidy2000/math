def mat_mult(A, B, mod=None):
    result = [
        [A[0][0]*B[0][0] + A[0][1]*B[1][0], A[0][0]*B[0][1] + A[0][1]*B[1][1]],
        [A[1][0]*B[0][0] + A[1][1]*B[1][0], A[1][0]*B[0][1] + A[1][1]*B[1][1]]
    ]
    if mod:
        result = [[x % mod for x in row] for row in result]
    return result

def mat_pow(M, n, mod=None):
    result = [[1,0],[0,1]]
    base = [row[:] for row in M]
    while n > 0:
        if n % 2 == 1:
            result = mat_mult(result, base, mod)
        base = mat_mult(base, base, mod)
        n //= 2
    return result

def mat_order(M, mod):
    """Find the order of matrix M in GL(2, Z/pZ)."""
    identity = [[1,0],[0,1]]
    power = [row[:] for row in M]
    for k in range(1, mod**4 + 1):
        power_mod = [[x % mod for x in row] for row in power]
        if power_mod == identity:
            return k
        power = mat_mult(power, M, mod)
    return None

