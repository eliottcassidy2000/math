from itertools import combinations, permutations

def count_3cycles(adj, n):
    """Count directed 3-cycles in tournament."""
    count = 0
    for a, b, c in combinations(range(n), 3):
        if adj[a][b] and adj[b][c] and adj[c][a]:
            count += 1
        if adj[a][c] and adj[c][b] and adj[b][a]:
            count += 1
    return count

def count_disjoint_3cycle_pairs(adj, n):
    """Count unordered pairs of vertex-disjoint 3-cycles."""
    # Find all 3-cycles (as frozensets of vertices)
    cycles_3 = []
    for a, b, c in combinations(range(n), 3):
        if adj[a][b] and adj[b][c] and adj[c][a]:
            cycles_3.append(frozenset([a,b,c]))
        elif adj[a][c] and adj[c][b] and adj[b][a]:
            cycles_3.append(frozenset([a,b,c]))
    # Note: each vertex triple contributes at most 1 cycle
    # Count disjoint pairs
    count = 0
    for i in range(len(cycles_3)):
        for j in range(i+1, len(cycles_3)):
            if not (cycles_3[i] & cycles_3[j]):  # disjoint
                count += 1
    return count

