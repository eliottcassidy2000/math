def compute_and_verify_palindrome(p, max_k=None):
    """Compute Omega dims and verify palindrome."""
    if max_k is None:
        max_k = p - 1

    A = paley_adj(p)
    cc = full_chain_complex_modp(A, p, max_p=max_k)
    omega = cc['omega_dims']

    print(f"\nT_{p}: Omega dims (up to k={max_k})")
    for k in range(max_k + 1):
        print(f"  Omega_{k} = {omega.get(k, 0)}")

    print(f"\nPalindrome check Omega_k = Omega_{{p-k}}:")
    for k in range(1, min(max_k + 1, p)):
        sym = p - k
        if sym <= max_k:
            ok = omega.get(k, 0) == omega.get(sym, 0)
            print(f"  Omega_{k}={omega.get(k,0)} vs Omega_{sym}={omega.get(sym,0)}: {'OK' if ok else 'FAIL'}")
        else:
            print(f"  Omega_{k}={omega.get(k,0)} vs Omega_{sym}=??? (not computed)")


