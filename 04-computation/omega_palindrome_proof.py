def paley_adj(p):
    qr = set()
    for i in range(1, p):
        qr.add((i * i) % p)
    A = [[0] * p for _ in range(p)]
    for i in range(p):
        for j in range(p):
            if i != j and (j - i) % p in qr:
                A[i][j] = 1
    return A


def check_self_converse(A, p):
    """Verify T_p is self-converse (T = T^op up to relabeling)."""
    # T^op has A^op[i][j] = A[j][i]
    # T_p is self-converse because the map i -> -i mod p sends arc (i,j) to (-i,-j)
    # and (-i)->(-j) iff (j-i) is QR iff (-j-(-i)) = -(j-i) is NQR for T^op
    # Wait: T_p has i->j iff (j-i) in QR. T_p^op has i->j iff (i-j) in QR iff -(j-i) in QR.
    # Since p=3 mod 4: -1 is NQR, so -(j-i) in QR iff (j-i) in NQR.
    # So T^op = complement of T (arcs reversed) = tournament where i->j iff (j-i) in NQR.
    # This is the Paley tournament with connection set NQR (same as relabeling i -> -i).
    # Under the map sigma: i -> -i mod p, the arc (i,j) in T maps to (-i,-j) in T^op.
    # T_p is self-converse, confirming sigma: T_p -> T_p^op is an isomorphism.

    # Verify: sigma(i) = -i mod p is an anti-automorphism
    # A[sigma(i)][sigma(j)] in T^op iff A[j][i] in T iff (i-j) in QR iff (j-i) in NQR
    # A[sigma(i)][sigma(j)] in T iff (-i -> -j) iff (-j - -i) in QR iff (j-i) in QR
    # For sigma to be anti-aut: (i->j in T) iff (sigma(j)->sigma(i) in T)
    # sigma(j)->sigma(i) = (-j)->(-i) iff (-i-(-j)) = j-i in QR iff i->j in T.
    print(f"T_{p} is self-converse via sigma: i -> -i mod p")

    # Verify numerically
    sigma = [((-i) % p) for i in range(p)]
    errors = 0
    for i in range(p):
        for j in range(p):
            if i == j: continue
            si, sj = sigma[i], sigma[j]
            # i->j iff sj->si (anti-aut = reversal isomorphism)
            if A[i][j] != A[sj][si]:
                errors += 1
    print(f"  Anti-automorphism verification: {errors} errors (should be 0)")
    return errors == 0


