print("\n--- Coboundaries as generators ---")

for n in [5, 6]:
    ne = n*(n-1)//2
    print(f"\n--- n={n} ---")

    dim_R_hist = Counter()
    tested = 0
    limit = None if n <= 5 else 500

    for bits in range(1 << ne):
        if limit and tested >= limit:
            break

        A = tournament_from_bits(n, bits)
        if beta1(A, n) != 0:
            continue

        edges = [(i,j) for i in range(n) for j in range(n) if i!=j and A[i][j]==1]
        tts = get_tts(A, n)
        mat = boundary2_matrix(A, n, edges, tts)
        rank_B1 = np.linalg.matrix_rank(mat, tol=1e-8)

        # Coboundary matrix δ: f ↦ (f(b)-f(a))
        cob = np.zeros((len(edges), n), dtype=float)
        for v in range(n):
            for i, (a, b) in enumerate(edges):
                if b == v: cob[i, v] += 1
                if a == v: cob[i, v] -= 1

        # When β₁=0: B₁ = Z₁ = coboundaries (all 3 are equal)
        # So coboundaries ARE B₁. dim(B₁/cob) = 0.
        # This is trivial.

        # What Grok might mean: quotient by SOME coboundaries?
        # Or: B₁ modulo the coboundary-generated subspace
        # But B₁ = coboundaries when β₁=0!

        # Actually, B₁ ⊂ Z₁ = coboundaries when β₁=0
        # And dim(B₁) = dim(Z₁) - β₁ = C(n,2)-(n-1)
        # And dim(coboundaries) = n-1
        # So B₁ ≠ coboundaries in general!
        # B₁ = im(∂₂), coboundaries = im(δ) = {f(b)-f(a) : f: V→Q}
        # These are DIFFERENT spaces!

        # When β₁=0: im(∂₂) = ker(∂₁) (since H₁=0)
        # ker(∂₁) has dim C(n,2)-(n-1)
        # im(δ) has dim n-1
        # These live in C₁ = R^{C(n,2)} and have different dimensions!

        # Wait... im(δ) ⊂ ker(∂₁)? Not necessarily for path complexes.
        # δf is a coboundary (cohomological), ∂₁ is homological boundary.

        # Actually in path homology: ∂₁(a,b) = b - a
        # δf(a,b) = f(b) - f(a) = ∂₁^*f applied to (a,b)
        # So δf ∈ im(∂₁^*), which is dual to im(∂₁).

        # Let me just compute: dim(B₁ ∩ im(δ))
        U_B, S_B, _ = np.linalg.svd(mat, full_matrices=True)
        B1_basis = U_B[:, :rank_B1]
        cob_in_B1 = B1_basis.T @ cob
        rank_cob_in_B1 = np.linalg.matrix_rank(cob_in_B1, tol=1e-8)
        dim_R = rank_B1 - rank_cob_in_B1

        dim_R_hist[dim_R] += 1
        tested += 1

    print(f"  Tested: {tested}")
    print(f"  dim(B₁ / (B₁∩im(δ))) = dim R: {dict(sorted(dim_R_hist.items()))}")
    print(f"  (This is B₁ modulo its intersection with coboundaries)")
