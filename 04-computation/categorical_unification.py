def count_23_trees(n):
    """Count trees with n leaves using binary and ternary internal nodes."""
    if n == 1:
        return 1  # single leaf
    total = 0
    # Binary node: partition n leaves into 2 groups
    for k in range(1, n):
        total += count_23_trees(k) * count_23_trees(n - k)
    # Ternary node: partition n leaves into 3 groups
