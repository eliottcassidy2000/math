def directed_odd_cycles(adj, n):
    """Find all directed odd cycles in tournament."""
    cycles = []
    # 3-cycles
    for triple in combs(range(n), 3):
        for perm in [(triple[0], triple[1], triple[2]),
                     (triple[0], triple[2], triple[1])]:
            if all((perm[i], perm[(i+1)%3]) in adj for i in range(3)):
                cycles.append(frozenset(perm))
                break  # only one direction can be a cycle
    # 5-cycles (for n >= 5)
    if n >= 5:
        for quint in combs(range(n), 5):
            from itertools import permutations as perms
            for perm in perms(quint):
                if all((perm[i], perm[(i+1)%5]) in adj for i in range(5)):
                    cycles.append(frozenset(perm))
                    break
    return cycles

def conflict_graph(cycles):
    """Build conflict graph: edges between cycles sharing vertices."""
    edges = set()
    cycle_list = list(cycles)
    for i in range(len(cycle_list)):
        for j in range(i+1, len(cycle_list)):
            if cycle_list[i] & cycle_list[j]:  # share vertices
                edges.add((i, j))
    return len(cycle_list), edges

def independence_poly(n_vertices, edges):
    """Compute independence polynomial of a graph."""
    # Find all independent sets
    coeffs = [0] * (n_vertices + 1)
    coeffs[0] = 1  # empty set

    adj_list = [set() for _ in range(n_vertices)]
    for i, j in edges:
        adj_list[i].add(j)
        adj_list[j].add(i)

    # Enumerate independent sets
    for size in range(1, n_vertices + 1):
        for subset in combs(range(n_vertices), size):
            independent = True
            for a, b in combs(subset, 2):
                if b in adj_list[a]:
                    independent = False
                    break
            if independent:
                coeffs[size] += 1

    return coeffs

