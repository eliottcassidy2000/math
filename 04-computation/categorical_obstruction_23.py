from itertools import combinations as combs

def generate_tournaments(n):
    """Generate all tournaments on n vertices as adjacency sets."""
    pairs = list(combs(range(n), 2))
    for bits in range(2**len(pairs)):
        adj = set()
        for k, (i, j) in enumerate(pairs):
            if bits & (1 << k):
                adj.add((i, j))
            else:
                adj.add((j, i))
        yield frozenset(adj)

def count_hamiltonian_paths(adj, n):
    """Count Hamiltonian paths in tournament given by adjacency set."""
    count = 0
    from itertools import permutations
    for perm in permutations(range(n)):
        valid = True
        for k in range(n - 1):
            if (perm[k], perm[k+1]) not in adj:
                valid = False
                break
        if valid:
            count += 1
    return count

