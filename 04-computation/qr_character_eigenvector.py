# Verify the QR action transitivity for specific primes
print("QR ACTION ON CHORD TYPES:")
for p in [7, 11, 19, 23, 31]:
    if p % 4 != 3:
        continue
    m = (p - 1) // 2
    qr_elts = [j for j in range(1, p) if is_qr(j, p)]

    # Compute QR orbits on {1,...,m}
    visited = set()
    orbits = []
    for k in range(1, m + 1):
        if k in visited:
            continue
        orbit = set()
        for a in qr_elts:
            ak = (a * k) % p
            if ak > m:
                ak = p - ak
            orbit.add(ak)
        orbits.append(sorted(orbit))
        visited.update(orbit)

    num_orbits = len(orbits)
    transitive = (num_orbits == 1)
    print(f"\n  p={p} (m={m}): QR action has {num_orbits} orbit(s)")
    for i, orb in enumerate(orbits):
        print(f"    Orbit {i+1}: {orb} (size {len(orb)})")
    print(f"    Transitive? {transitive}")

    # Also check: dimension of fixed subspace
    # For each QR a, compute the signed permutation matrix P_a
    dim = m
    P_matrices = []
    for a in qr_elts:
        P = np.zeros((m, m))
        for k in range(m):
            chord = k + 1
            ak = (a * chord) % p
            if ak <= m:
                target = ak - 1
                sign = 1
            else:
                target = p - ak - 1
                sign = -1
            P[target, k] = sign
        P_matrices.append(P)

    # Fixed subspace = intersection of ker(P_a - I) for all a
    # Stack equations (P_a - I) x = 0
    equations = np.vstack([P - np.eye(m) for P in P_matrices])
    rank = np.linalg.matrix_rank(equations, tol=1e-10)
    fixed_dim = m - rank
    print(f"    Fixed subspace dimension: {fixed_dim}")

    # Check sigma_P is in the fixed subspace
    sigma_P = np.array([1 if is_qr(k, p) else -1 for k in range(1, m + 1)], dtype=float)
    residual = np.max(np.abs(equations @ sigma_P))
    print(f"    sigma_P in fixed subspace? {residual < 1e-10} (max residual = {residual:.2e})")

    # If fixed_dim > 1, what are the other fixed vectors?
    if fixed_dim > 1:
        # SVD of equations
        U, s, Vt = np.linalg.svd(equations)
        # Null space = last fixed_dim rows of Vt
